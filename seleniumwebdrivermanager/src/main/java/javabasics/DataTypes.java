package javabasics;

public class DataTypes {

	public static void main(String[] args) {
		
		//primitive data types: boolean,char,byte,short,int,long,float,double
		
				boolean b = true;
				boolean b1 = false; //default value is false, 1byte
				
				char c = 'M';// 2 bytes
				
				byte b2 = 36; //range -128 to 127, 1 byte
				short s = 32000;//range -32768 to 32767, 2 bytes
				long l = 1999999999;//8 bytes
				int i = 75;//4 bytes
				
				float f = (float) -3.402;//4 bytes
				float f1 = 5.96f;
				double d = 0.08;//8 bytes
				
				//downcasting
				int a = (int) 45.68;

	}

}
